const num1 = 25;
const num2 = 15;

let ans;

console.log(" ==== Simple Calculator ==== ")
ans = num1 + num2;
console.log(`Additon: ${num1} + ${num2} = ${ans}`);

ans = num1 - num2;
console.log(`Subtraction: ${num1} - ${num2} = ${ans}`);

ans = num1 * num2;
console.log(`Multiplication: ${num1} * ${num2} = ${ans}`);

ans = num1 / num2;
console.log(`Division: ${num1} / ${num2} = ${ans}`);

ans = num1 % num2;
console.log(`Modulus: ${num1} % ${num2} = ${ans}`);